    private void tablaContenidoMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaContenidoMousePressed
        tablaContenido.addMouseListener(new MouseAdapter() {
            public void mouseClicked(MouseEvent e) {
                int fila = tablaContenido.rowAtPoint(e.getPoint());
                int columna = tablaContenido.columnAtPoint(e.getPoint());
                if ((fila > -1) && (columna > -1)) {
                    id = (int) tablaContenido.getValueAt(fila, 1);
                    nV = (int) tablaContenido.getValueAt(fila, 0);
                    articulos = (int) tablaContenido.getValueAt(fila, 4);
                    botonDevolver.setEnabled(true);
                }
            }
        });
    }//GEN-LAST:event_tablaContenidoMousePressed

