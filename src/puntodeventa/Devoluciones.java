    private void botonDevolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonDevolverActionPerformed
        int opc = JOptionPane.showConfirmDialog(this, "¿Desea devolver estos productos?");
        if (opc == JOptionPane.YES_OPTION) {
            try {
                String SQL = "INSERT INTO devolucionventas(\n"
                        + "	id_prd, id_vnt, devvt_date, canart)\n"
                        + "	VALUES (?, ?, ?, ?);";
                PreparedStatement devolucion = conn.prepareStatement(SQL);
                devolucion.setInt(1, id);
                devolucion.setInt(2, nV);

                Calendar date = Calendar.getInstance();
                SimpleDateFormat dateformatter = new SimpleDateFormat("yyyy-MM-dd");
                fecha = dateformatter.format(date.getTime());
                JLabel etq2 = new JLabel(fecha);

                devolucion.setDate(3, java.sql.Date.valueOf(etq2.getText()));
                devolucion.setInt(4, articulos);

                devolucion.executeUpdate();
                JOptionPane.showMessageDialog(this, "Productos devueltos con éxito");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Ocurrió un error con la devolución.\n" + e);
            }
            try {
                String SQL2 = "UPDATE productos"
                        + "SET"
                        + "     prd_exist = prd_exist + ?"
                        + "WHERE "
                        + "     id_prd = " + id + "";
                PreparedStatement actualizar = conn.prepareStatement(SQL2);
                actualizar.setInt(1, articulos);
                JOptionPane.showMessageDialog(this, "Actualización de la cantidad exitosa.");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Ocurrió un error con la actualización de la existencia.\n" + e);
            }
            try {
                dev.consultaVentasGen(tablaContenido);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Error en la consulta de las ventas\n" + ex);
            } catch (IOException ex) {
                Logger.getLogger(Devoluciones.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {

        }
    }//GEN-LAST:event_botonDevolverActionPerformed

    private void busquedaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_busquedaKeyTyped
        char car = evt.getKeyChar();
        if ((car < '0' || car > '9') && (car < 'A' || car > 'Z') && (car < 'a' || car > 'z') && (car < '-' || car > '-')) {
            evt.consume();
        }
    }//GEN-LAST:event_busquedaKeyTyped

